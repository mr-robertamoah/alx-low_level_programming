
/**
* add - add two integers
* @a: integer
* @b: integer
* Return: integer
*/
int add(int a, int b)
{
	return (a + b);
}

/**
* sub - subtract two integers
* @a: integer
* @b: integer
* Return: integer
*/
int sub(int a, int b)
{
	return (a - b);
}

/**
* mul - product two integers
* @a: integer
* @b: integer
* Return: integer
*/
int mul(int a, int b)
{
	return (a * b);
}

/**
* div - quotient two integers
* @a: integer
* @b: integer
* Return: integer
*/
int div(int a, int b)
{
	return (a / b);
}

/**
* mod - remainder of two integers
* @a: integer
* @b: integer
* Return: integer
*/
int mod(int a, int b)
{
	return (a % b);
}
